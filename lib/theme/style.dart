import 'package:flutter/material.dart';

// define app theme
ThemeData appTheme() {
  return ThemeData(
      primaryColor: Colors.orange,
      appBarTheme:
          const AppBarTheme(backgroundColor: Color.fromARGB(255, 10, 6, 61)));
}

// define app theme
ThemeData darkAppTheme() {
  return ThemeData(
